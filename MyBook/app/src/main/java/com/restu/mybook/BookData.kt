    fun getIndex(thisBook: Book): Int {
        for (position in bookTitle.indices) {
            if (thisBook.title == bookTitle[position]) {
                return position
            }
        }
        return -1
    }
    fun getFav(thisBook: Book) {
        val position = getIndex(thisBook)
        bookFav[position] = !bookFav[position]
        thisBook.isFav = !thisBook.isFav
